/**
 * @brief  发送1个字节到串口
 * @param  t: 要发送的字节
 * @retval None
 */
void SendChar(uint8_t t)
{
    USART3->DR = t;
    while ((USART3->SR & 0x40) == 0);  // 等待发送结束
}

